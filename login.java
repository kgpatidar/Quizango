    private void codeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_codeKeyTyped
        int ccod = evt.getKeyChar();
        if(s>3){
            s = 3;
            evt.consume();
            getToolkit().beep();
        }
        if((evt.getKeyChar() == KeyEvent.VK_BACK_SPACE || evt.getKeyChar() == KeyEvent.VK_DELETE) && s>0){
//            System.out.println("Back   " + s);
            --s;
        }
        if(!(Character.isDigit(ccod))){
            evt.consume();
            getToolkit().beep();
        }else{
            s++;
        }
    }//GEN-LAST:event_codeKeyTyped

