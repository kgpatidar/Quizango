    private void rollsKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_rollsKeyTyped
        int ccod = evt.getKeyChar();
        if(l>5){
            l = 5;
            evt.consume();
            getToolkit().beep();
        }
        if((evt.getKeyChar() == KeyEvent.VK_BACK_SPACE || evt.getKeyChar() == KeyEvent.VK_DELETE) && l>0){
//            System.out.println("Back   " + s);
            --l;
        }
        if(!(Character.isDigit(ccod))){
            evt.consume();
            getToolkit().beep();
        }else{
            l++;
        }
    }//GEN-LAST:event_rollsKeyTyped

